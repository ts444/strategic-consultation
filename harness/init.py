#!/usr/bin/env python3
"""Scaffold a new engagement repo for the strategic-consulting harness."""

import argparse
import re
import subprocess
import sys
from pathlib import Path

PHASE_DIRS = [
    "00-intake",
    "01-situation",
    "02-gap",
    "03-mapping",
    "04-roadmap",
    "05-handover",
    "06-retro",
]

# Minimal empty register stubs (frontmatter + header only; no entries)
