def _read_harness_version() -> str:
    """Extract the first version number from the harness CHANGELOG.md."""
    changelog = Path(__file__).parent.parent / "CHANGELOG.md"
    if not changelog.exists():
        return "unknown"
    text = changelog.read_text(encoding="utf-8")
    match = re.search(r"##\s+\[(\d+\.\d+\.\d+)\]", text)
    return match.group(1) if match else "unknown"


def _scaffold(customer: str, target: Path, version: str) -> None:
    target.mkdir(parents=True, exist_ok=False)

    # Phase directories
    for phase in PHASE_DIRS:
        (target / phase).mkdir()

    # Root registers
    for filename, stub in _REGISTER_STUBS.items():
        (target / filename).write_text(
            stub.format(version=version, customer=customer),
            encoding="utf-8",
        )

    # Engagement CLAUDE.md
    (target / "CLAUDE.md").write_text(
        _CLAUDE_MD_STUB.format(version=version, customer=customer),
        encoding="utf-8",
    )


def _git_init_and_commit(target: Path, customer: str) -> None:
    env_extras: dict[str, str] = {}
    subprocess.run(["git", "init"], cwd=target, check=True, capture_output=True)
    subprocess.run(["git", "add", "."], cwd=target, check=True, capture_output=True)
    subprocess.run(
        ["git", "commit", "-m", f"chore: scaffold engagement for {customer}"],
        cwd=target,
        check=True,
        capture_output=True,
        env={**__import__("os").environ, **env_extras},
    )


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        description="Scaffold a new engagement repo.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument("customer_name", help="Customer / engagement name (slug-safe)")
    parser.add_argument(
        "--target-dir",
        default=None,
        help="Directory to create the engagement repo in (default: ./<customer-name>)",
    )
    args = parser.parse_args(argv)

    customer: str = args.customer_name
    target = Path(args.target_dir) if args.target_dir else Path(customer)

    if target.exists() and any(target.iterdir()):
        print(
            f"error: target directory '{target}' already exists and is not empty.",
            file=sys.stderr,
        )
        return 1

    version = _read_harness_version()

    try:
        _scaffold(customer, target, version)
    except FileExistsError:
        print(
            f"error: target directory '{target}' already exists and is not empty.",
            file=sys.stderr,
        )
        return 1

    try:
        _git_init_and_commit(target, customer)
    except subprocess.CalledProcessError as exc:
        print(f"error: git init/commit failed: {exc}", file=sys.stderr)
        return 1

    print(f"Engagement repo created at: {target.resolve()}")
    print(f"  harness_version: {version}")
    print("  Edit CLAUDE.md to fill in compliance frameworks, budget, and stakeholders.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
